package myelseif;

public class Div {
	    public static void main(String[] args) {
	        int num = 15;
	        if (num % 2 == 0) {
	            System.out.println("Divisible by 2");
	        } else if (num % 3 == 0) {
	            System.out.println("Divisible by 3");
	        } else if (num % 5 == 0) {
	            System.out.println("Divisible by 5");
	        } else {
	            System.out.println("Not divisible by 2, 3, or 5");
	        }
	    }
	
}
