package myif;

public class Positive {
	    public static void main(String[] args) {
	        int num = 10;
	        if (num > 0) {
	            System.out.println("Positive");
	        }
	        if (num < 0) {
	            System.out.println("Negative");
	        }
	        if (num == 0) {
	            System.out.println("Zero");
	        }
	    }


}
