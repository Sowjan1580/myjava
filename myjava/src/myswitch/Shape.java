package myswitch;

public class Shape {
	    public static void main(String[] args) {
	        int shape = 2;
	        switch (shape) {
	            case 1:
	                System.out.println("Circle");
	                break;
	            case 2:
	                System.out.println("Rectangle");
	                break;
	            case 3:
	                System.out.println("Triangle");
	                break;
	            default:
	                System.out.println("Invalid shape");
	        }
	}

}
